package conectorcompleto;

public class Tabla1 {
    //Variables por cada uno de los campos de la tabla
    private int id;
    private String nombre;
    private String apellido;
    private int edad;
//Metodos get y set por cada uno de los campos de la tabla

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
}
