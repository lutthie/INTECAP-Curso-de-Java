    //MÃ©todo para mostrar los datos de una tabla de la Base de Datos
    protected void mostrar(){
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnCount(0);
        model.addColumn("Id");
        model.addColumn("Nombre");
        model.addColumn("Apellido");
        model.addColumn("Edad");
        
        Consultorio cl = new Consultorio();
        for (Tabla1 t : cl.consultarTabla()) {
            model.addRow(new Object[]{t.getId(),t.getNombre(),t.getApellido(),t.getEdad()});
        }
        tblMostrar.setModel(model);
        
    }
    
